export function buildStrategyUserPrompt(context: ResearchContext): string {
  const serialized = serializeContext(context)

  return `На основе исследовательского контекста подготовь стратегический отчёт по правилам из system-промпта.

КОНТЕКСТ ИССЛЕДОВАНИЯ:
${serialized}

Структура — ровно 5 разделов с обязательными заголовками:

## 1. Анализ бизнеса
Карточки актива «🏢 Сама компания» + «🏢 Сайт» + «🏢 Каждый указанный канал присутствия (отдельная карточка для каждого Telegram-канала, бота, ВК-группы и т.д.)».

ОБЯЗАТЕЛЬНО — карточки конкурентов. Для каждого конкурента (и тех, кого указал клиент, и тех, кого выявило исследование):

**🏆 [Название конкурента] — 📌 Указан клиентом** (или **🔍 Найден в research**)

✅ Что у них сильнее: [конкретно]
❌ Где они слабее или уязвимы: [конкретно]
🎯 Что перенять или превзойти: [конкретное действие]
🤖 AI-рычаг (если есть): [как AI поможет обогнать]

## 2. Анализ рынка
Не общие тренды, а: какие конкретные изменения в нише создают окно роста именно для этой компании? Размер рынка / динамика / регуляторика — только релевантные ИХ бизнесу.

## 3. Анализ целевой аудитории
Карточки по сегментам ЦА. Для каждого: ✅ что у компании уже работает на этот сегмент, ❌ что не работает, 🎯 что сделать, 🤖 AI-рычаг (если применим).

## 4. Анализ каналов
Карточки актива по каждому каналу. Не «развивайте Telegram», а: «Канал @ruznak публикует 2 поста/нед — бенчмарк B2B-маркировки 5–7. Что добавить и какой контент.»

## 5. Стратегия и рекомендации
Приоритезированный план в 3 горизонтах:
- 🔴 **Эта неделя (0 ₽):** что можно сделать без вложений
- 🟡 **Этот месяц (бюджет до X ₽):** конкретные действия с суммами
- 🟢 **3 месяца (стратегические инициативы):** инициативы с бюджетами

В этом разделе AI-рычаги показываются как **отдельный приоритетный блок «🤖 Топ AI-возможностей»** — только реальные, со сценариями «было/стало» и оценками экономии в ₽ и часах.

═══════════════════════════════
НАПОМИНАНИЕ:
- Ни одной банальности. Если нет конкретики — пропусти, не натягивай.
- AI-рычаг — только реальный. Но если он есть — подавай как ключевую возможность.
- Каждая карточка привязана к [ФАКТ] из контекста. Без факта — карточки нет.
═══════════════════════════════`
}
