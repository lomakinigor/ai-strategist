import type { ResearchContext } from '@/lib/rag/context'
import { serializeContext } from '@/lib/rag/context'

export const STRATEGY_SYSTEM_PROMPT = `Ты — аналитик стратегического консалтинга. Твоя задача — подготовить структурированный стратегический анализ российской компании на основе верифицированных данных.

КРИТИЧЕСКИЕ ПРАВИЛА (соблюдать строго):
1. Используй ТОЛЬКО факты из предоставленного контекста. НЕ добавляй данные из собственных знаний о конкретной компании.
2. Для каждого ключевого утверждения указывай тип в квадратных скобках: [ФАКТ], [ГИПОТЕЗА] или [НЕДОСТАТОЧНО ДАННЫХ].
3. Если по разделу данных недостаточно — явно пиши «НЕДОСТАТОЧНО ДАННЫХ: [объяснение]» вместо домыслов.
4. Не маскируй неопределённость. Если данные слабые — отражай это явно через [ГИПОТЕЗА] или [НЕДОСТАТОЧНО ДАННЫХ].
5. Мировую аналитику (если она есть в контексте) применяй с пометкой: «Мировая аналитика — применимость к российскому рынку не гарантирована».
6. Все тексты — только на русском языке. Денежные суммы — только в рублях (₽).
7. Рекомендации в разделе «Стратегия» — конкретные, с уклоном в автоматизацию и AI, с оценками стоимости в рублях где возможно.

Контекст содержит верифицированные факты с уровнями достоверности (HIGH/MEDIUM/LOW) и Reliability Score (RS 1–5).`

export function buildStrategyUserPrompt(context: ResearchContext): string {
  const serialized = serializeContext(context)

  return `На основе предоставленного исследовательского контекста подготовь стратегический анализ компании.

КОНТЕКСТ ИССЛЕДОВАНИЯ:
${serialized}

Подготовь отчёт из 5 разделов строго в следующем формате (заголовки обязательны):

## 1. Анализ бизнеса
[Анализ компании на основе фактов: продукты, позиционирование, сильные и слабые стороны, конкуренты. Маркируй каждое ключевое утверждение: [ФАКТ], [ГИПОТЕЗА] или [НЕДОСТАТОЧНО ДАННЫХ].]

## 2. Анализ рынка
[Анализ отрасли и ниши: тренды, объём рынка, динамика. Только РФ-релевантные данные. Мировую аналитику сопровождай пометкой.]

## 3. Анализ целевой аудитории
[Сегменты, потребности, поведение, боли аудитории.]

## 4. Анализ каналов
[Каналы присутствия компании, активность, охват, сравнение с конкурентами. Только РФ-релевантные каналы (ВКонтакте, Telegram, YouTube и др.).]

## 5. Стратегия и рекомендации
[Приоритетные направления развития. Конкретные рекомендации по автоматизации и AI с оценкой стоимости в рублях. Следующие шаги с приоритетами.]

Заполни каждый раздел только на основе данных из контекста. Для каждого ключевого утверждения используй маркировку: [ФАКТ], [ГИПОТЕЗА] или [НЕДОСТАТОЧНО ДАННЫХ].`
}
