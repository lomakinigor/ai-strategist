export function buildSectionSystemPrompt(sectionType: StrategySectionType): string {
  const title = SECTION_TITLES[sectionType]
  return `Ты — аналитик стратегического консалтинга. Тебе поручено написать ТОЛЬКО раздел «${title}» стратегического отчёта для российской компании.

${COMMON_RULES}

═══════════════════════════════════════════════════════════════
ИНСТРУКЦИЯ ДЛЯ ЭТОГО РАЗДЕЛА
═══════════════════════════════════════════════════════════════

${SECTION_INSTRUCTIONS[sectionType]}

ФОРМАТ ОТВЕТА:
- Только тело раздела в markdown.
- НЕ добавляй заголовок «## N. ...» — он добавится позже.
- НЕ пиши преамбулу/постамбулу. Сразу карточки актива.
- Если не хватает данных — лучше 1 качественная карточка, чем 5 натянутых.`
}

export function buildSectionUserPrompt(
  sectionType: StrategySectionType,
  context: ResearchContext,
): string {
  const block = getBlockByType(context, sectionType)
  const blockText = block?.contextText ?? `=== ${SECTION_TITLES[sectionType]} ===\nДанных нет.`

  // Также включаем минимальный кросс-контекст: компания/индустрия/каналы — берём из business-блока
  const businessBlock =
    sectionType !== 'business' ? getBlockByType(context, 'business') : undefined

  const contextParts: string[] = []
  if (businessBlock && businessBlock.factCount > 0) {
    contextParts.push(
      `КРАТКИЙ КОНТЕКСТ О КОМПАНИИ (для ориентации, не основной материал раздела):\n${businessBlock.contextText}`,
    )
  }
  contextParts.push(`ОСНОВНОЙ КОНТЕКСТ ДЛЯ РАЗДЕЛА:\n${blockText}`)

  return `Напиши тело раздела «${SECTION_TITLES[sectionType]}» по правилам system-промпта.

${contextParts.join('\n\n')}

Верни ТОЛЬКО markdown-тело раздела, без заголовка «## ...» и без преамбулы.`
}

