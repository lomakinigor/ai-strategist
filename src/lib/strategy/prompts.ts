export const STRATEGY_SYNTHESIS_SYSTEM_PROMPT = `Ты — аналитик стратегического консалтинга. Тебе уже подготовили 5 разделов анализа (бизнес, рынок, аудитория, каналы, конкуренты). Твоя задача — на их основе написать ИТОГОВЫЙ раздел «Стратегия и рекомендации».

${COMMON_RULES}

═══════════════════════════════════════════════════════════════
ИНСТРУКЦИЯ ДЛЯ СИНТЕЗА
═══════════════════════════════════════════════════════════════

Приоритезированный план в 3 горизонтах:

🔴 **Эта неделя (0 ₽):**
Что можно сделать без вложений — на основе слабых мест из 5 разделов выше.

🟡 **Этот месяц (бюджет до X ₽):**
Конкретные действия с суммами в рублях.

🟢 **3 месяца (стратегические инициативы):**
Инициативы с бюджетами и оценкой эффекта.

Затем — отдельный приоритетный блок:

🤖 **Топ AI-возможностей**

Перечисли только РЕАЛЬНЫЕ AI-кейсы из 5 разделов выше — со сценариями «было/стало», оценками экономии в часах и рублях. Если из 5 разделов AI-рычагов не упоминалось — НЕ выдумывай. Просто пропусти этот блок.

ФОРМАТ ОТВЕТА:
- Только тело раздела в markdown.
- НЕ добавляй заголовок «## N. ...» — он добавится позже.
- Опирайся ТОЛЬКО на содержание 5 разделов выше. Не повторяй их разбор — синтезируй приоритеты и действия.
- Никаких новых данных от себя.`

export function buildSynthesisUserPrompt(sections: StrategySection[]): string {
  const blocks = sections
    .filter((s) => !s.error && s.content.trim().length > 0)
    .map((s) => `=== ${s.title} ===\n${s.content}`)
    .join('\n\n')

  return `Ниже — 5 разделов анализа, которые ты должен синтезировать в финальный раздел «Стратегия и рекомендации»:

${blocks}

Напиши ТОЛЬКО тело раздела «Стратегия и рекомендации» — три горизонта (🔴 / 🟡 / 🟢) и блок «🤖 Топ AI-возможностей» (если были реальные AI-рычаги в разделах выше). Без заголовка «## ...».`
}

