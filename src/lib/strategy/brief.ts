import { callStrategyLLM } from './generator'

const BRIEF_SYSTEM_PROMPT = `Ты аналитик-стратег. Создай краткое резюме стратегического анализа.

МЕТОДОЛОГИЯ:
- Minto (Принцип пирамиды): вывод первым, затем 3 факта-обоснования, тест «И что?» — каждый факт должен давать действие
- McKinsey Way: 80/20 — только 20% самой важной информации, elevator test — раздел понятен за 30 секунд
- Knaflic (Storytelling with Data): Контекст → Инсайт → Данные, убирай лишнее, одна ключевая мысль на раздел

ФОРМАТ КАЖДОГО РАЗДЕЛА (строго):
**[Название раздела]**
ВЫВОД: [одно предложение — главный инсайт с конкретным утверждением]
• [Факт 1 — конкретный, с цифрой или именем]
• [Факт 2]
• [Факт 3]
ДЕЙСТВИЕ: [конкретный следующий шаг — глагол + объект + срок/метрика]

ПРАВИЛА:
- Пропускай целые разделы, если в них только [НЕДОСТАТОЧНО ДАННЫХ]
- Пропускай отдельные факты с [НЕДОСТАТОЧНО ДАННЫХ] или [ГИПОТЕЗА] без источника
- Только конкретные данные — никаких общих слов («необходимо», «важно», «следует рассмотреть»)
- Суммы только в рублях (₽)
- Весь отчёт — не более 500 слов
- Разделы: Бизнес | Рынок | Аудитория | Каналы | Конкуренты | Стратегия (только те, где есть данные)`

export async function generateBriefReport(contentMarkdown: string): Promise<string> {
  const userPrompt =
    `На основе стратегического анализа ниже создай краткое резюме строго по методологии (Minto + McKinsey + Knaflic).\n\n` +
    `АНАЛИЗ:\n${contentMarkdown}\n\n` +
    `Помни: пропускай всё что помечено [НЕДОСТАТОЧНО ДАННЫХ]. Включай только факты с конкретными данными.`

  const { content } = await callStrategyLLM(BRIEF_SYSTEM_PROMPT, userPrompt)
  return content
}
